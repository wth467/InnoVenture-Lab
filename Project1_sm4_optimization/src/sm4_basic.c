// 循环左移
static inline uint32_t rotl(uint32_t x, int n) {
    return (x << n) | (x >> (32 - n));
}

// S盒替换
static uint32_t sbox_sub(uint32_t x) {
    uint32_t res = 0;
    res |= (uint32_t)SBOX[(x >> 24) & 0xFF] << 24;
    res |= (uint32_t)SBOX[(x >> 16) & 0xFF] << 16;
    res |= (uint32_t)SBOX[(x >> 8) & 0xFF] << 8;
    res |= (uint32_t)SBOX[x & 0xFF];
    return res;
}

// 加密线性变换L
static uint32_t linear_trans(uint32_t x) {
    return x ^ rotl(x, 2) ^ rotl(x, 10) ^ rotl(x, 18) ^ rotl(x, 24);
}

// 密钥扩展中的线性变换L'
static uint32_t key_linear_trans(uint32_t x) {
    return x ^ rotl(x, 13) ^ rotl(x, 23);
}

// 密钥扩展
void sm4_key_expansion(const uint8_t mk[16], uint32_t rk[32]) {
    const uint32_t FK[4] = {0xA3B1BAC6, 0x56AA3350, 0x677D9197, 0xB27022DC};
    const uint32_t CK[32] = {
        0x00070E15, 0x1C232A31, 0x383F464D, 0x545B6269,
        0x70777E85, 0x8C939AA1, 0xA8AFB6BD, 0xC4CBD2D9,
        0xE0E7EEF5, 0xFC030A11, 0x181F262D, 0x343B4249,
        0x50575E65, 0x6C737A81, 0x888F969D, 0xA4ABB2B9,
        0xC0C7CED5, 0xDCE3EAF1, 0xF8FF060D, 0x141B2229,
        0x30373E45, 0x4C535A61, 0x686F767D, 0x848B9299,
        0xA0A7AEB5, 0xBCC3CAD1, 0xD8DFE6ED, 0xF4FB0209,
        0x10171E25, 0x2C333A41, 0x484F565D, 0x646B7279
    };
    
    uint32_t k[36];
    
    // 加载主密钥
    for (int i = 0; i < 4; i++) {
        k[i] = ((uint32_t)mk[4*i] << 24) | 
               ((uint32_t)mk[4*i+1] << 16) | 
               ((uint32_t)mk[4*i+2] << 8) | 
               mk[4*i+3];
        k[i] ^= FK[i];
    }
    
    // 密钥扩展
    for (int i = 0; i < 32; i++) {
        uint32_t t = k[i+1] ^ k[i+2] ^ k[i+3] ^ CK[i];
        t = sbox_sub(t);
        t = key_linear_trans(t);
        k[i+4] = k[i] ^ t;
        rk[i] = k[i+4];
    }
}

// 基础加密函数
void sm4_encrypt(const uint8_t key[16], const uint8_t in[16], uint8_t out[16]) {
    uint32_t rk[32];
    sm4_key_expansion(key, rk);
    
    uint32_t block[4];
    for (int i = 0; i < 4; i++) {
        block[i] = ((uint32_t)in[4*i] << 24) | 
                  ((uint32_t)in[4*i+1] << 16) | 
                  ((uint32_t)in[4*i+2] << 8) | 
                  in[4*i+3];
    }
    
    // 32轮迭代
    for (int r = 0; r < 32; r++) {
        uint32_t t = block[1] ^ block[2] ^ block[3] ^ rk[r];
        t = sbox_sub(t);
        t = linear_trans(t);
        uint32_t new_block = block[0] ^ t;
        
        // 移位
        block[0] = block[1];
        block[1] = block[2];
        block[2] = block[3];
        block[3] = new_block;
    }
    
    // 输出（逆序）
    for (int i = 0; i < 4; i++) {
        out[4*i]   = (block[3-i] >> 24) & 0xFF;
        out[4*i+1] = (block[3-i] >> 16) & 0xFF;
        out[4*i+2] = (block[3-i] >> 8) & 0xFF;
        out[4*i+3] = block[3-i] & 0xFF;
    }
}

