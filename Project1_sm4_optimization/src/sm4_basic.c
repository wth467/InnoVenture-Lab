// 基础解密函数
void sm4_decrypt(const uint8_t key[16], const uint8_t in[16], uint8_t out[16]) {
    uint32_t rk[32];
    sm4_key_expansion(key, rk);
    
    // 使用逆序轮密钥
    uint32_t decrypt_rk[32];
    for (int i = 0; i < 32; i++) {
        decrypt_rk[i] = rk[31 - i];
    }
    
    uint32_t block[4];
    for (int i = 0; i < 4; i++) {
        block[i] = ((uint32_t)in[4*i] << 24) | 
                  ((uint32_t)in[4*i+1] << 16) | 
                  ((uint32_t)in[4*i+2] << 8) | 
                  in[4*i+3];
    }
    
    // 32轮迭代（使用逆序轮密钥）
    for (int r = 0; r < 32; r++) {
        uint32_t t = block[1] ^ block[2] ^ block[3] ^ decrypt_rk[r];
        t = sbox_sub(t);
        t = linear_trans(t);
        uint32_t new_block = block[0] ^ t;
        
        // 移位
        block[0] = block[1];
        block[1] = block[2];
        block[2] = block[3];
        block[3] = new_block;
    }
    
    // 输出（逆序）
    for (int i = 0; i < 4; i++) {
        out[4*i]   = (block[3-i] >> 24) & 0xFF;
        out[4*i+1] = (block[3-i] >> 16) & 0xFF;
        out[4*i+2] = (block[3-i] >> 8) & 0xFF;
        out[4*i+3] = block[3-i] & 0xFF;
    }
}